/// An enumeration representing different statuses for tasks.
///
/// This enum defines four statuses for tasks: ToDo, InProgress, InReview, and Done.
/// It can be used to indicate the current state of a task during its lifecycle.
enum Status {
  ToDo, // Task is yet to be started.
  InProgress, // Task is currently being worked on.
  InReview, // Task is under review or verification.
  Done // Task has been completed.
}
